public class Typecasting{
    public static void main(String[]args) {
        int i=5;
        System.out.println(i);
        double j=i;
        System.out.println(j);
        double p=12.34;
        System.out.println(p);
        int q=(int) p;
        System.out.println(q);
        
        
    }
}