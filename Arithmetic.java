public class Arithmetic{
    public static void main(String[]args){
        int n1,n2,sum,sub,mult,div,rem;
        n1=23;
        n2=6;
        sum=n1+n2;
        System.out.println("The sum of these numbers is" +sum);
        sub=n1-n2;
        System.out.println("The sub of these numbers is " + sub);
        mult=n1*n2;
        System.out.println("The multipication of these numbers is" + mult);
        div=n1/n2;
        System.out.println("The division of these numbers is" + div);
        rem=n1%n2;
        System.out.println("The remainder of these numbers is"+ rem);
    }
}