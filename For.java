 public class For{
    public static void main(String[]args) {
        for(int i=0;i<=10;i++){
            System.out.println(i);
        }
        for(int i=90;i>=40;i--){
        
        System.out.println(i);
        }
        
        for(int i=-50;i<=50;i++){
        
        System.out.println(i);
        }
        for(int i=1;i<=1000;i*=3){
            System.out.println(i);   
        }

        
    }
}